"""
generator.py — генератор бесконечных случайных уровней.

ГЛАВНАЯ ИДЕЯ (важно для понимания):
Если просто раскидать тайлы случайно — почти всегда получится НЕпроходимый
уровень (стена до потолка, яма шире прыжка и т.п.). Поэтому мы не бросаем
кубики вслепую, а ведём "шагающего строителя" слева направо. Он строит
дорогу по правилам, которые гарантируют проходимость:

  * ступеньки вверх — не выше прыжка (≈3 тайла), берём максимум 2 для запаса;
  * ямы (пропасти) — не шире прыжка, максимум 3 тайла;
  * обязательный путь идёт по НЕЙТРАЛЬНЫМ блокам '#', которые твёрдые всегда,
    поэтому полярность не может "сломать" дорогу;
  * цветные блоки B/O ставятся только как НЕОБЯЗАТЕЛЬНЫЕ платформы-бонусы
    с кристаллом сверху. Хочешь — допрыгни и собери, не хочешь — иди мимо.

Функция возвращает обычный текст в том же формате, что и levels/*.txt,
поэтому его можно сразу передать в Level(text) без всяких переделок.
"""
import random
import settings as S

# Безопасные пределы, подобранные под текущую физику прыжка.
# (прыжок поднимает примерно на 3 тайла и переносит примерно на 5 в длину)
MAX_STEP_UP = 2      # на сколько строк вверх может подняться "пол" за раз
MAX_GAP = 3          # максимальная ширина ямы в тайлах


def generate_level(seed=None, length=70):
    """Строит случайный проходимый уровень и возвращает его как текст.

    seed   — число для повторяемости (одинаковый seed = одинаковый уровень).
             None = каждый раз новый уровень.
    length — примерная ширина уровня в тайлах.
    """
    if seed is not None:
        random.seed(seed)

    W = max(24, length)
    H = S.GRID_H                       # высота уровня = высота экрана (18 тайлов)
    grid = [["." for _ in range(W)] for _ in range(H)]

    lo = H - 3                         # самый низкий допустимый "пол"
    hi = H - 8                         # самый высокий допустимый "пол"
    g = H - 4                          # текущая строка пола

    def put_ground(x, top):
        """Заполняем колонку x землёй '#' от строки top до самого низа."""
        for y in range(top, H):
            grid[y][x] = "#"

    # --- стартовая площадка: 4 тайла ровного пола ---
    for x in range(4):
        put_ground(x, g)
    grid[g - 1][1] = "@"               # точка старта игрока (над полом)

    x = 4
    while x < W - 5:
        roll = random.random()

        if roll < 0.18:
            # --- ЯМА: пропасть (пустые колонки = падение и смерть, если не перепрыгнуть)
            gap = min(random.randint(1, MAX_GAP), W - 8 - x)
            if gap < 1:
                gap = 1
            # иногда перекинем нейтральный мост '#' (он безопаснее — можно идти пешком)
            bridge = random.random() < 0.45
            for _ in range(gap):
                if bridge:
                    grid[g][x] = "#"   # мост на уровне пола
                x += 1
            # ВАЖНО: после ямы ВСЕГДА кладём площадку приземления (минимум 2 тайла).
            # Без этого две ямы подряд могли слиться в непрыгабельную пропасть.
            for _ in range(random.randint(2, 4)):
                if x >= W - 5:
                    break
                put_ground(x, g)
                x += 1

        elif roll < 0.38:
            # --- СТУПЕНЬКА ВВЕРХ (в пределах прыжка) ---
            g = max(hi, g - random.randint(1, MAX_STEP_UP))
            for _ in range(random.randint(2, 4)):
                if x >= W - 5:
                    break
                put_ground(x, g)
                x += 1

        elif roll < 0.55:
            # --- СТУПЕНЬКА ВНИЗ ---
            g = min(lo, g + random.randint(1, 2))
            for _ in range(random.randint(2, 4)):
                if x >= W - 5:
                    break
                put_ground(x, g)
                x += 1

        else:
            # --- РОВНЫЙ УЧАСТОК (с возможными шипами/кристаллами) ---
            seg = random.randint(2, 5)
            for i in range(seg):
                if x >= W - 5:
                    break
                put_ground(x, g)
                # первый тайл сегмента оставляем чистым (честное место для приземления)
                if i > 0:
                    if random.random() < 0.10:
                        grid[g - 1][x] = "^"          # одиночный шип — перепрыгивается
                    elif random.random() < 0.16:
                        grid[g - 2][x] = "*"          # кристалл над землёй
                x += 1

        # --- иногда: бонусная цветная платформа с кристаллом (необязательная) ---
        if random.random() < 0.18 and g - 4 > 0:
            col = random.choice(["B", "O"])
            px = min(x, W - 6)
            grid[g - 3][px] = col
            grid[g - 4][px] = "*"

    # --- финишная площадка + выход ---
    for xx in range(W - 5, W):
        put_ground(xx, g)
    grid[g - 1][W - 3] = "E"

    # сетка -> текст
    return "\n".join("".join(row) for row in grid)
