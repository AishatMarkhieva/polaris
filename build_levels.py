"""
build_levels.py — генератор файлов уровней.

Это вспомогательный инструмент (не часть игры). Он строит уровни из
"кирпичиков" (пол, блоки, шипы, кристаллы) и записывает их в levels/*.txt.
Так удобнее гарантировать ровную сетку и проходимость, чем рисовать руками.

Ты можешь:
  * править эти уровни здесь и пересобирать (python build_levels.py),
  * или редактировать готовые levels/*.txt напрямую в текстовом редакторе.
"""
import os

W, H = 40, 18
LEVELS_DIR = os.path.join(os.path.dirname(__file__), "levels")


class Grid:
    def __init__(self, w=W, h=H):
        self.w, self.h = w, h
        self.g = [["." for _ in range(w)] for _ in range(h)]

    def set(self, x, y, ch):
        if 0 <= x < self.w and 0 <= y < self.h:
            self.g[y][x] = ch

    def hline(self, x0, x1, y, ch):
        for x in range(x0, x1 + 1):
            self.set(x, y, ch)

    def rect(self, x0, y0, x1, y1, ch):
        for y in range(y0, y1 + 1):
            for x in range(x0, x1 + 1):
                self.set(x, y, ch)

    def floor(self, x0, x1, top_y, ch="#"):
        """Сплошной пол от строки top_y до низа."""
        for y in range(top_y, self.h):
            self.hline(x0, x1, y, ch)

    def text(self):
        return "\n".join("".join(row) for row in self.g) + "\n"


def save(name, grid):
    os.makedirs(LEVELS_DIR, exist_ok=True)
    with open(os.path.join(LEVELS_DIR, name), "w", encoding="utf-8") as f:
        f.write(grid.text())


# ---------------------------------------------------------------------------
# УРОВЕНЬ 1 — обучение: ходьба, прыжок, сбор кристаллов, выход.
# Без опасностей. Везде есть пол (упасть нельзя).
# ---------------------------------------------------------------------------
def level_1():
    g = Grid()
    g.floor(0, 39, 16)                 # сплошной нижний пол
    # пара ступенек-бугров для тренировки прыжка
    g.rect(11, 14, 13, 15, "#")
    g.rect(20, 14, 22, 15, "#")
    # кристаллы над землёй (легко достать)
    g.set(7, 13, "*")
    g.set(21, 11, "*")
    g.set(30, 14, "*")
    g.set(2, 14, "@")                  # старт
    g.set(36, 15, "E")                 # выход
    return g


# ---------------------------------------------------------------------------
# УРОВЕНЬ 2 — знакомство с полярностью.
# Игрок стартует СИНИМ. Синие блоки (B) твёрдые, оранжевые (O) — сквозные.
# Между цветными мостами есть нейтральные '#' островки — на них безопасно
# переключать полярность (стоя на '#', ты не провалишься).
# ---------------------------------------------------------------------------
def level_2():
    g = Grid()
    # старт
    g.rect(0, 16, 6, 17, "#")
    g.set(2, 15, "@")
    # СИНИЙ мост над пропастью (row17 пустой => провал, если ты не синий)
    g.hline(7, 11, 16, "B")
    # нейтральный островок: тут переключаемся на оранжевый
    g.rect(12, 16, 12, 17, "#")
    g.set(12, 14, "*")
    # ОРАНЖЕВЫЙ мост над пропастью
    g.hline(13, 19, 16, "O")
    # нейтральный островок
    g.rect(20, 16, 21, 17, "#")
    # ровная земля до выхода
    g.rect(22, 16, 39, 17, "#")
    # необязательный кристалл на синей платформе (нужно снова стать синим)
    g.hline(26, 29, 13, "B")
    g.set(27, 12, "*")
    g.set(37, 15, "E")
    return g


# ---------------------------------------------------------------------------
# УРОВЕНЬ 3 — первые шипы + чередование цветов.
# Внизу полоса шипов. Над ней — дорожка из блоков: сначала синие,
# затем нейтральный '#' (тут меняем цвет), затем оранжевые.
# ---------------------------------------------------------------------------
def level_3():
    g = Grid()
    g.floor(0, 39, 17)                 # нижняя опора по всей ширине
    g.hline(0, 6, 16, "#")             # левая земля
    g.hline(7, 26, 16, "^")            # шипы посередине
    g.hline(27, 39, 16, "#")           # правая земля
    g.set(2, 15, "@")
    # дорожка над шипами на высоте row13
    g.hline(7, 9, 13, "B")
    g.hline(11, 13, 13, "B")           # синий участок
    g.set(15, 13, "#")                 # нейтральный островок — смена цвета
    g.hline(17, 19, 13, "O")
    g.hline(21, 23, 13, "O")           # оранжевый участок
    g.set(25, 13, "#")                 # нейтральный островок
    # кристаллы над дорожкой
    g.set(8, 12, "*")
    g.set(18, 12, "*")
    g.set(22, 12, "*")
    g.set(35, 15, "E")
    return g


# ---------------------------------------------------------------------------
# УРОВЕНЬ 4 — финал: подъём вверх + шипы + переключения. Карта шире и выше
# экрана, поэтому камера прокручивается. Нейтральные '#' — точки смены цвета.
# ---------------------------------------------------------------------------
def level_4():
    g = Grid(w=48, h=20)
    g.floor(0, 47, 19)                 # нижняя опора
    g.hline(0, 5, 18, "#")             # старт-земля
    g.hline(6, 30, 18, "^")            # шипы внизу
    g.hline(31, 47, 18, "#")           # безопасная земля справа
    g.set(2, 17, "@")
    # дорожка над шипами (синие, островок, оранжевые) — ступень вверх 2 тайла
    g.hline(6, 8, 16, "B")
    g.hline(10, 12, 16, "B")
    g.set(14, 16, "#")                 # островок: смена цвета
    g.hline(16, 18, 16, "O")
    g.hline(20, 22, 16, "O")
    g.set(24, 16, "#")                 # островок
    g.set(11, 15, "*")
    g.set(21, 15, "*")
    # лестница вверх — каждая ступень на 2 тайла выше (нейтральные, всегда твёрдые)
    g.hline(26, 28, 14, "#")
    g.hline(30, 32, 12, "#")
    g.hline(34, 36, 10, "#")
    # верхняя дорожка с одним шипом-препятствием и кристаллами
    g.hline(34, 42, 10, "#")
    g.set(39, 9, "^")
    g.set(41, 9, "*")
    g.set(35, 9, "*")
    # справа — спуск: просто шагаешь с края на безопасную землю, затем к выходу
    g.set(45, 17, "E")
    return g


def main():
    save("level_01.txt", level_1())
    save("level_02.txt", level_2())
    save("level_03.txt", level_3())
    save("level_04.txt", level_4())
    print("Уровни сгенерированы в", LEVELS_DIR)


if __name__ == "__main__":
    main()
